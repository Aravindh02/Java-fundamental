package precourse;

public class loops {
	public static void main(String[] args) {
		
		for(int i=1; i<=30; i=i+1) {
			System.out.println(i);
		}

		int m=1;
		while(m<=20) {
			System.out.println(m);
			m=m+1;
		}
	
		int j=1;
		do {
			System.out.println(j);
			j=j+1;
		}
			while(j<=10);
			
	}

}
