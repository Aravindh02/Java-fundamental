package precourse;

public class operator {
	public static void main(String[] args) {    
        int j = 25;    
        int o = 15;    
        int res = j + o;    
        System.out.println("j + o = " + res);    
        res = j - o;    
        System.out.println("j - o = " + res);    
        res = j * o;    
        System.out.println("j * o = " + res);    
        res = j / o;    
        System.out.println("j / o = " + res);    
        res = j % 2;    
        System.out.println("j % 2 = " + res);    
    }  

}
